#include <QMessageBox>

#include "modifyconstraintstudentsmingapsbetweenroomchangesform.h"

ModifyConstraintStudentsMinGapsBetweenRoomChangesForm::ModifyConstraintStudentsMinGapsBetweenRoomChangesForm(QWidget* parent, ConstraintStudentsMinGapsBetweenRoomChanges* ctr): QDialog(parent)
{
	setupUi(this);

	okPushButton->setDefault(true);

	connect(okPushButton, SIGNAL(clicked()), this, SLOT(ok()));
	connect(cancelPushButton, SIGNAL(clicked()), this, SLOT(cancel()));

	centerWidgetOnScreen(this);
	restoreFETDialogGeometry(this);
	
	this->_ctr=ctr;
	
	weightLineEdit->setText(CustomFETString::number(ctr->weightPercentage));
	
	minGapsSpinBox->setMinimum(1);
	minGapsSpinBox->setMaximum(gt.rules.nHoursPerDay);
	minGapsSpinBox->setValue(ctr->minGapsBetweenRoomChanges);
}

ModifyConstraintStudentsMinGapsBetweenRoomChangesForm::~ModifyConstraintStudentsMinGapsBetweenRoomChangesForm()
{
	saveFETDialogGeometry(this);
}

void ModifyConstraintStudentsMinGapsBetweenRoomChangesForm::ok()
{
	double weight;
	QString tmp=weightLineEdit->text();
	weight_sscanf(tmp, "%lf", &weight);
	if(weight<100.0 || weight>100.0){
		QMessageBox::warning(this, tr("FET information"),
			tr("Invalid weight (percentage). It has to be 100"));
		return;
	}

	QString oldcs=this->_ctr->getDetailedDescription(gt.rules);

	QString newcs=this->_ctr->getDetailedDescription(gt.rules);
	gt.rules.addUndoPoint(tr("Modified the constraint:\n\n%1\ninto\n\n%2").arg(oldcs).arg(newcs));

	this->_ctr->weightPercentage=weight;
	this->_ctr->minGapsBetweenRoomChanges=minGapsSpinBox->value();

	gt.rules.internalStructureComputed=false;
	setRulesModifiedAndOtherThings(&gt.rules);
	
	this->close();
}

void ModifyConstraintStudentsMinGapsBetweenRoomChangesForm::cancel()
{
	this->close();
}
